 CREATE TABLE HOD
(
    -- COL_NAME1 DATA_TYPE [CONSTRAINT_TYPE] ,

    BRANCH CHAR(2) PRIMARY KEY,
    H_NAME VARCHAR(20),
    SALARY NUMBER CHECK(SALARY>0),
    DOJ DATE NOT NULL,
    MNO NUMBER(10) UNIQUE CHECK(LENGTH(MNO)=10)
);

CREATE TABLE STUDENT
(
    SID NUMBER(3) PRIMARY KEY,
    SNAME VARCHAR(20),
    BRANCH CHAR(2) DEFAULT ('CS'),
    YOP NUMBER(4) CHECK (YOP>2024),
    DOB DATE,
    EMAIL VARCHAR(30) CHECK(EMAIL LIKE '%@%') UNIQUE,
    MNO NUMBER(10) UNIQUE CHECK (LENGTH(MNO)=10),
    PER NUMBER(4,2) NOT NULL

    -- CONSTRAINT  REF_NAME FOREIGN KEY (COL_NAME) REFRENCES PARENT_TABLE (COL_NAME)
    CONSTRAINT  BRANCH_FK FOREIGN KEY (BRANCH) REFRENCES HOD (BRANCH)
);



