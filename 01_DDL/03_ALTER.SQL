-- STATEMENT'S USED TO MODIFY THE STRUCTURE OF THE TABLE

-- 1: TO ADD A COLUMN
SYNTAX: ALTER TABLE TABLE_NAME
        ADD COL_NAME DATA_TYPE [CONSTRAINT];

ALTER TABLE STUD 
ADD COLLEGE VARCHAR(30) DEFAULT('HIT CLG');


-- 2: TO DROP A COLUMN
SYNTAX: ALTER TABLE TABLE_NAME
        DROP COLUMN COLUMN_NMAE;

ALTER TABLE STUD
DROP COLUMN COLLEGE;


-- 3: TO RENAME A COLUMN
SYNTAX: ALTER TABLE TABLE_NAME
        RENMAE COLUMN OLD_COL_NAME TO NEW_COL_NAME

ALTER TABLE STUD 
RENAME COLUMN PERC TO SCORE;

ALTER TABLE HOD
RENAME COLUMN MNO TO PHNO;


-- 4 TO MODIFY THE DATATYPE
SYNTAX: ALTER TABLE TABLE_NAME
        MODIFY COLUMN DATA_TYPE;

ALTER TABLE STUD
MODIFY SCORE NUMBER(5,2);

ALTER TABLE STUD
MODIFY SNAME VARCHAR(30);


-- 5: TO ADD CONSTRAINT
SYNTAX: ALTER TABLE TABLE_NAME
        ADD CONSTRAINT CONSTRAINT_REF_NAME
        CONSTRAINT_TYPE (COL);

ALTER TABLE HOD
ADD CONSTRAINT PHNO_UQ
UNIQUE (PHNO);

ALTER TABLE TABLE_NAME
ADD CONSTRAINT CHK_PHNO
CHECK (LENGTH(PHNO)=10);


-- 6: TO DROP CONSTRAINT
SYNTAX: ALTER TABLE TABLE_NAME
        DROP CONSTRAINT CONSTRAINT_REF_NAME

ALTER TABLE HOD_DATA
DROP SYS_(0055624) PHNO_UQ;


-- 7: TO MODIFY CONSTRAINT
SYNTAX: ALTER TABLE TABLE_NAME
        MODIFY COL_NAME EXISTING_DATA_TYPE CONSTRAINT;

ALTER TABLE TABLE_NAME
MODIFY SNAME VARCHAR(30) NOT NULL;
MODIFY MNO NUMBER(10) NOT NULL;
MODIFY SCORE NUMBER(5,2) CHECK (SCORE>=70);
MODIFY BRANCH CHAR(2) DEFAULT ('EC');


-- 8: ENABLE / DISABLE CONSTRAINT
SYNTAX: ALTER TABLE TABLE_NAME
        DISABLE CONSTRAINT CONSTRAINT_REF_NAME;
        ENABLE CONSTRAINT CONSTRAINT_REF_NAME;

ALTER TABLE TABLE_NAME
DISABLE CONSTRAINT CHK_PHNO;

ALTER TABLE TABLE_NAME
ENABLE CONSTRAINT CHK_PHNO;