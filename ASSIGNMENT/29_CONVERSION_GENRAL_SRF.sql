-- 2️⃣ GENERAL
--1 WAQTD TOTAL SAL OF EMPS
SELECT SAL + COMM
FROM SCOTT.EMP;

-- WITH NVL(NULL VALUE LOGIC)
SELECT SAL + NVL(COMM, 0)
FROM SCOTT.EMP;

-- NVL2
--1 WAQTD NAME & MGRID FOR ALL THE EMPS IF THE EMP IS HAVING MGRID PRINT 1 ELSE PRINT 0
SELECT ENAME, MGR, NVL2 (MGR, 1, 0) 
FROM SCOTT.EMP;

--2 WAQTD NAME & COMM FOR THE EMPS IF THE EMP IS HAVING COMM PRINT 100 ELSE PRINT 200
SELECT ENAME , NVL2(COMM, 100, 200)
FROM SCOTT.EMP;