-- 1️⃣ CONVERSION
--1 WAQTD NAME, HIREDATE AND HIREYEAR IF EMP HIRED IN YEAR 1981
SELECT HIREDATE, TO_CHAR(HIREDATE, 'YYYY')
FROM SCOTT.EMP
WHERE TO_CHAR(HIREDATE, 'YYYY') = 1981;

--2 NAME HIREDATE AND HIREMONTH FOR THE EMPS IF THE EMP HIRED ON SEPTEMBER OR APRIL
SELECT ENAME, HIREDATE, TO_CHAR(HIREDATE,'MON')
FROM SCOTT.EMP
-- WHERE TO_CHAR(HIREDATE,'MON') IN ('SEP', 'APR');
WHERE TO_CHAR(HIREDATE,'MM') IN (9, 4);

--3 WAQTD NAME HIREEDATE, HIREDAY FOR THE EMPS IF THE EMP HIRED ON WENESDAY OR FRIDAY
SELECT ENAME , HIREDATE, TO_CHAR(HIREDATE,'DAY')
FROM SCOTT.EMP
WHERE TO_CHAR(HIREDATE,'D') IN (4, 6);

