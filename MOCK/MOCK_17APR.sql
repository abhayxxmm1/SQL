SELECT DISTINCT ENAME, SAL, SAL*12
FROM SCOTT.EMP
WHERE SAL BETWEEN 2000 AND 3000 AND
        SAL*12 > 29400;

SELECT ENAME, DEPTNO
FROM SCOTT.EMP
WHERE DEPTNO IN (10, 40, 30, 20) AND 
        JOB NOT IN ('SALESMAN', 'MANAGER') AND
        HIREDATE > '31-DEC-82';

SELECT EMP.*, SAL*6
FROM SCOTT.EMP
WHERE (HIREDATE LIKE '%FEB%' OR
        HIREDATE LIKE '%DEC%') AND
        EMPNO NOT BETWEEN '7369' AND '7521';

SELECT COUNT(*) AS NUM_EMP, DEPTNO
FROM SCOTT.EMP
WHERE HIREDATE LIKE '%81' AND
        ENAME LIKE '%E%'
GROUP BY DEPTNO;

SELECT COUNT(*), MAX(SAL), MIN(SAL), SUM(SAL), DEPTNO
FROM SCOTT.EMP
WHERE COMM IS NULL AND 
        MGR IS NOT NULL
GROUP BY DEPTNO 
HAVING COUNT(*) >= 2 AND
        MAX(SAL) > 2450 AND 
        (MIN (SAL) = 800 OR
        MIN(SAL) = 950) AND 
        (SUM(SAL) LIKE '____' OR
        SUM(SAL) LIKE '_____');

SELECT COUNT(*), JOB
FROM SCOTT.EMP
WHERE JOB LIKE '%L%' OR
        JOB LIKE '%N%'
GROUP BY JOB
HAVING COUNT(*) >= 2 AND 
        MAX(SAL) LIKE '____' AND 
        MIN(SAL) NOT BETWEEN 100 AND 1000 AND
        MAX(SAL) NOT BETWEEN 100 AND 1000
ORDER BY JOB;

SELECT 'MR.' || ENAME || 'YOU' || HIREDATE || 'AND WORKING AS A ' || JOB || 'WITH 23% HIKE OF SALARY' || (SAL + SAL*0.23)
FROM SCOTT.EMP
WHERE HIREDATE < '01-JAN-82' AND 
        HIREDATE NOT LIKE '%-SEP-81' AND 
        JOB IN ('ANALYST', 'PRESIDENT');


