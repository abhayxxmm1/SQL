SELECT ENAME, SAL
FROM SCOTT.EMP
WHERE (ENAME LIKE '%A%' OR
        ENAME LIKE '%E%' OR
        ENAME LIKE '%I%' OR
        ENAME LIKE '%O%' OR 
        ENAME LIKE '%U%') AND
        DEPTNO != 20 AND
        SAL BETWEEN 2450 AND 3000 
ORDER BY ENAME DESC;

SELECT MAX(SAL), MIN(SAL), SUM(SAL), COUNT(*)
FROM SCOTT.EMP
WHERE SAL IS NOT NULL AND
        ENAME NOT LIKE '%S%' AND
       DEPTNO IN (10, 30);

