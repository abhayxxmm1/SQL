FROM EMP
WHERE SUBSTR(ENAME, 2,1) IN ('A', 'E', 'I', 'O', 'U') AND
JOB LIKE '%AN%' AND
DEPTNO NOT IN (SELECT DEPTNO
                FROM EMP
                WHERE ENAME = 'TURNER');

-- WAQTD EMP NAME , MGR’ NAME, MGR’S MGR NAME ALONG WITH
-- THEIR LOCATIONS IF MGR & MGR’S MGR WORKING IN DIFFERENT
-- LOCATIONS & EMP IS WORKING IN RESEARCH DEPT.
