-- 7] WAQTD MANAGER’S DNAME OF EMP WHO IS EARNING 7TH MAX SAL
SELECT D2.DNAME
FROM EMP E1 JOIN EMP E2
ON E1.MGR = E2.EMPNO
INNER JOIN DEPT D1 ON E1.DEPTNO = D1.DEPTNO
INNER JOIN DEPT D2 ON E2.DEPTNO = D2.DEPTNO
WHERE (SELECT COUNT (DISTINCT SAL)
        FROM EMP E2
        WHERE E1.SAL < E2.SAL) = 6;

-- WAQTD NUMBER OF EMP’S, MAX SAL, MIN SAL & TOTAL SAL FOR EACH
-- DESIGNATION IF EMP’S NOT EARNING ANY COMMISSION, EXCEPT THE
-- EMP’S HIRED IN THE YEAR ‘81’ AND EACH DESIGNATION IS HAVING LESS
-- THAN 5000 RPS MAXIMUM SALARY & TOTAL SALARY IN EACH
-- DESIGNATION MUST BE AT MOST 6000 RPS.
SELECT COUNT(*), MAX(SAL), MIN(SAL), SUM(SAL), JOB 
FROM EMP
WHERE COMM IS NULL AND
    HIREDATE NOT LIKE '%81'
GROUP BY JOB
HAVING MAX(SAL) < 5000 AND
        SUM(SAL) <= 6000;

-- 9] WAQTD NAME, HIREDATE, HIRED DAY & DEPTNO OF EMP’S WHO IS
-- NOT HIRED IN THE LEAP YEAR BUT HIRED ON SATURDAY OR TUESDAY
-- WORKING IN NEW YORK.
SELECT E.ENAME, E.HIREDATE, TO_CHAR(E.HIREDATE, 'DAY') AS HIRED_DAY, E.DEPTNO
FROM EMP E INNER JOIN DEPT D ON E.DEPTNO = D.DEPTNO
WHERE MOD(TO_NUMBER(TO_CHAR(E.HIREDATE, 'YYYY')),4) != 0 
        AND TO_CHAR(E.HIREDATE, 'DY') IN ('SAT', 'TUE') AND
        D.LOC = 'NEWYORK';

SELECT 
    E.ENAME,
    E.HIREDATE,
    TO_CHAR(E.HIREDATE, 'DAY') AS HIRED_DAY,
    E.DEPTNO
FROM 
    EMP E
    JOIN DEPT D ON E.DEPTNO = D.DEPTNO
WHERE 
    D.LOC = 'NEW YORK'
    AND TO_CHAR(E.HIREDATE, 'DAY') IN ('TUESDAY  ', 'SATURDAY ')
    AND MOD(TO_NUMBER(TO_CHAR(E.HIREDATE, 'YYYY')), 4) != 0;