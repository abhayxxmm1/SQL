-- WAQTD DETAILS OF EMP’S WHO IS HAVING VOWELS IN 2ND PLACE
-- AND DESIGNATION IS HAVING A STRING ‘AN’ IN IT EXCEPT THE DEPTNO
-- IN WHICH ‘TURNER’ IS WORKING
