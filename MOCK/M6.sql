    A.APP_NAME, 
    A.RELEASE_DATE, 
    AL.LIKES, 
    R.RATINGS,
    D.DWNLD
FROM 
    APPS A
    INNER JOIN APP_LIKES AL ON A.LIKE_ID = AL.LIKE_ID
    INNER JOIN DOWNLOADS D ON A.APP_ID = D.APP_ID
    INNER JOIN RATING R ON AL.RATING_ID = R.RATING_ID
WHERE 
    D.DWNLD >= 1.5
    AND AL.LIKES < 4
    AND A.RELEASE_DATE > '31-DEC-2017'
    AND R.RATINGS > 2;

-- 3] WAQTD NUMBER OF APP’S ALONG WITH THE LIKES WHICH IS HAVING
-- AT MOST 5M LIKES.
SELECT COUNT(APP_NAME), AL.LIKES
FROM APPS A 
    INNER JOIN APP_LIKES AL ON A.LIKE_ID = AL.LIKE_ID
    INNER JOIN DOWNLOADS D ON A.APP_ID = D.APP_ID
    INNER JOIN RATING R ON AL.RATING_ID = R.RATING_ID
WHERE AL.LIKES <= 5
GROUP BY AL.LIKES;

